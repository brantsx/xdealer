function listing(
  listingIndex: number,
  vehicle: Vehicle,
  profileId: string,
  status: MarketplaceListing["status"],
  type: MarketplaceListing["listingType"],
  price: number,
  options: Partial<MarketplaceListing> = {},
): MarketplaceListing {
  const decision = vehicle.decisionPack;
  return {
    id: `listing-${String(listingIndex).padStart(2, "0")}`,
    organisationId: vehicle.organisationId,
    vehicleId: vehicle.id,
    sellerProfileId: profileId,
    listingType: type,
    status,
    title: `${vehicle.make} ${vehicle.model} ${vehicle.derivative}`,
    description:
      options.description ??
      "Trade listing created from the xDealer appraisal pack with condition disclosure, pricing guardrails and buyer questions handled inside the workspace.",
    askingPrice: price,
    reservePrice: Math.max(0, price - 450),
    buyNowPrice: type === "Buy it now" ? price + 400 : undefined,
    minimumOffer: Math.max(0, price - 650),
    bidIncrement: 100,
    currentHighestBid: type === "Timed auction" ? price - 250 : options.currentHighestBid,
    startsAt: new Date(new Date(now).getTime() - listingIndex * 6 * 60 * 60 * 1000).toISOString(),
    endsAt: type === "Timed auction" ? new Date(new Date(now).getTime() + (listingIndex % 5 + 1) * day).toISOString() : undefined,
    visibilityType: "All approved dealers",
    location: options.location ?? (vehicle.organisationId === PREMIUM_ORG_ID ? "Reading" : vehicle.organisationId === ESTATE_ORG_ID ? "Nottingham" : "Manchester"),
    postcodeArea: options.postcodeArea ?? (vehicle.organisationId === PREMIUM_ORG_ID ? "RG2" : vehicle.organisationId === ESTATE_ORG_ID ? "NG8" : "M17"),
    vatMarginNote: options.vatMarginNote ?? "Margin scheme. No VAT qualifying invoice in this demo listing.",
    buyerFeeNote: options.buyerFeeNote ?? "No platform buyer fee in MVP; transport and any buyer fees handled outside Xdealer.",
    sellerDeclarationAccepted: status !== "Draft",
    bodyworkSummary: `${vehicle.appraisal.paintwork}. ${vehicle.damageEntries.length} damage item${vehicle.damageEntries.length === 1 ? "" : "s"} disclosed.`,
    interiorSummary: `${vehicle.appraisal.interior}. ${vehicle.numberOfKeys} key${vehicle.numberOfKeys === 1 ? "" : "s"} present.`,
    mechanicalSummary: vehicle.appraisal.mechanicalNotes,
    knownIssues: vehicle.motAdvisories.length ? vehicle.motAdvisories.join("; ") : "No known mechanical issues beyond normal used vehicle appraisal.",
    prepRecommendation: decision?.prepRecommendation ?? "Prepare to trade standard and price cosmetic work transparently.",
    auditNotes: ["Listing pre-filled from appraisal", "Seller edited pricing and declaration before publish"],
    documents: ["V5C status note", "Mock appraisal PDF", "Mock HPI summary"],
    views: 24 + listingIndex * 7,
    watchers: listingIndex % 4 === 0 ? 9 : 2 + (listingIndex % 7),
    publishedAt: status === "Draft" ? undefined : new Date(new Date(now).getTime() - listingIndex * 4 * 60 * 60 * 1000).toISOString(),
    soldAt: status === "Sold" ? new Date(new Date(now).getTime() - day).toISOString() : undefined,
    withdrawnAt: status === "Withdrawn" ? new Date(new Date(now).getTime() - day).toISOString() : undefined,
    createdAt: new Date(new Date(now).getTime() - listingIndex * day).toISOString(),
    updatedAt: now,
    ...options,
  };
}

