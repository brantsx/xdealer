function id(index: number): string {
  return `b2000000-0000-4000-8000-${String(index).padStart(12, "0")}`;
}

function photo(vehicleId: string, index: number, orgId: string): VehiclePhoto {
  return {
    id: `${vehicleId.slice(0, 30)}p${index}`,
    organisationId: orgId,
    vehicleId,
    fileName: `marketplace-${index}.svg`,
    storagePath: `marketplace/${vehicleId}/${index}.svg`,
    publicUrl: "/assets/vehicle-placeholder.svg",
    caption: index === 1 ? "Front three-quarter" : "Condition evidence",
    createdAt: now,
  };
}

function appraisal(vehicleId: string, orgId: string, qualityScore: number, mechanicalNotes: string, warningLights = ""): Appraisal {
  return {
    id: `${vehicleId.slice(0, 27)}app`,
    organisationId: orgId,
    vehicleId,
    tyres: qualityScore > 78 ? "Good" : "Needs prep",
    alloys: qualityScore > 72 ? "Minor wear" : "Needs prep",
    glass: "Good",
    interior: qualityScore > 70 ? "Good" : "Minor wear",
    paintwork: qualityScore > 76 ? "Minor wear" : "Needs prep",
    mechanicalNotes,
    warningLights,
    appraiserId: DEMO_USER_ID,
    qualityScore,
    createdAt: now,
    updatedAt: now,
  };
}

function damage(vehicleId: string, orgId: string, index: number, panel: string, type: string, cost: number): DamageEntry {
  return {
    id: `${vehicleId.slice(0, 27)}d${index}`,
    organisationId: orgId,
    vehicleId,
    panelLocation: panel,
    damageType: type,
    severity: cost > 600 ? "Heavy" : cost > 250 ? "Medium" : "Light",
    estimatedRepairCategory: cost > 600 ? "Panel repair" : cost > 250 ? "Paint" : "Smart repair",
    estimatedCost: cost,
    notes: "Disclosed for trade pricing and buyer appraisal.",
    createdAt: now,
    updatedAt: now,
  };
}

